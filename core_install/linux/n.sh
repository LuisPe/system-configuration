#!/bin/bash
set -e

echo "################################################################"
echo "#############              INSTALLING N         ################"
echo "################################################################"

curl -L https://git.io/n-install | N_PREFIX=$HOME/.local/n bash -s -- -y

echo 'export N_PREFIX="$HOME/.local/n"; [[ :$PATH: == *":$N_PREFIX/bin:"* ]] || PATH+=":$N_PREFIX/bin"' >>$HOME/.zshrc

echo "################################################################"
echo "#############              N INSTALLED          ################"
echo "################################################################"
